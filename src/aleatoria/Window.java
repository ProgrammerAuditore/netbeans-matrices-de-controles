    private void btnRunMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRunMouseReleased
        // TODO add your handling code here:
        try{
            this.NumMaximo = Integer.parseInt(this.campo1.getText());
            this.NumMinimo = Integer.parseInt(this.campo2.getText());
            
            if( (NumMinimo < 0 || NumMaximo > 22) ){
                JOptionPane.showMessageDialog(null, "Error, introduzca un numero entre de 0 a 22.");
            }else if((NumMinimo == 0 && NumMaximo == 0)){
                JOptionPane.showMessageDialog(null, "Error, ambos números no pueden ser 0.");
            }else if ( NumMinimo > NumMaximo){
                JOptionPane.showMessageDialog(null, "Error, el número mínimo es mayor al número máximo.");
            }else{
                this.fncLimpiarPanel();
                this.fncAleatoria();
                this.panelRoom.validate();
                this.panelRoom.repaint();
            }
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro, solo se aceptan números enteros positivos, incluyendo el cero.");
        }
        
    }//GEN-LAST:event_btnRunMouseReleased

