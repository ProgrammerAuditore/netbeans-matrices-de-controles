    private void fncAleatoria(){
        int separadorH = 0, separadorV = 0;
        int cantidad = this.getIntRandom( this.NumMinimo, this.NumMaximo);
        this.campo3.setText(""+ cantidad);
        
        for(int item=0; item < cantidad; item++){
            // Crear un panel
            JPanel pR = new JPanel();
            
            // Establecer color aleatorio
            pR.setBackground(new Color( this.getIntRandom(0,250), this.getIntRandom(0,250), this.getIntRandom(0,250)));
 
            // Establecer separacion de 
            if( (separadorH + 100) >=  panelRoom.getWidth() ){
                separadorH = 0;
                separadorV += 120;
            }
            
            // Establecer tamaño aleatorio
            int tamahnos = this.getIntRandom(50,100); 
            pR.setBounds(separadorH , separadorV, tamahnos , tamahnos);
            
            this.panelRoom.add(pR);
            
            separadorH += tamahnos + 100; 
        }
    
    }
    
    private void fncLimpiarPanel(){
        this.panelRoom.removeAll();
        this.panelRoom.validate();
        this.panelRoom.repaint();
    }
    
    private int getIntRandom(int x_min, int x_max){
        return ((int)((x_max - x_min + 1) * Math.random()) + x_min);
    }
    
